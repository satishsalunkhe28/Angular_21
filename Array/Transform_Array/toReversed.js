// toReversed  example   

let letters = ['A', 'B', 'C', 'D', 'E'];
console.log("Original Array: ", letters);
// Using toReversed to create a reversed copy of the array
let reversedLetters = letters.toReversed();
console.log("Reversed Array using toReversed: ", reversedLetters);


// Original array remains unchanged
console.log("Original Array after toReversed: ", letters);
console.log("======================================================")   
let numbers = [1, 2, 3, 4, 5];
console.log("Original Number Array: ", numbers);        

// Using toReversed to create a reversed copy of the number array
let reversedNumbers = numbers.toReversed();
console.log("Reversed Number Array using toReversed: ", reversedNumbers);

// Original array remains unchanged
console.log("Original Number Array after toReversed: ", numbers);
